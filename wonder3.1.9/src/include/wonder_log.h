#ifndef SWONDERLOG_H
#define SWONDERLOG_H

#include <string>
#include <syslog.h>

// Currently syslog ( the system logger ) is used to log messages.
// If you preferr to use another logging mechanism change this class.

class WonderLog
{
public:
    WonderLog();
    WonderLog( const char *name );
    ~WonderLog();

    // use LOG_INFO, LOG_DEBUG or LOG_ERR as priority
    void print( int priority, std::string msg );

    // This defines which messages are logged. In verbose mode all the 
    // messages are logged in mode error only the critical ones.
    // modes:  0=error 1=debug 2=verbose
    void setMode( int mode ); 
};

#endif
